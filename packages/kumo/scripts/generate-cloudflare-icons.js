/**
 * Generates Cloudflare icon sprite + typed glyph names from repo-owned SVG assets.
 *
 * Source of truth: src/icons/assets/*.svg
 * Generated output:
 * - src/icons/generated/sprite.svg
 * - src/icons/generated/names.ts
 */

import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, extname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const ASSETS_DIR = join(__dirname, "../src/icons/assets");
const GENERATED_DIR = join(__dirname, "../src/icons/generated");
const SPRITE_PATH = join(GENERATED_DIR, "sprite.svg");
const NAMES_PATH = join(GENERATED_DIR, "names.ts");

export function normalizeIconName(fileName) {
  return fileName
    .replace(/\.svg$/i, "")
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .replace(/[\s_]+/g, "-")
    .replace(/[^a-zA-Z0-9-]+/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "")
    .toLowerCase();
}

export function extractViewBox(svgOpenTag) {
  const viewBoxMatch = svgOpenTag.match(/viewBox=["']([^"']+)["']/i);
  if (viewBoxMatch) {
    return viewBoxMatch[1];
  }

  const widthMatch = svgOpenTag.match(/width=["']([^"']+)["']/i);
  const heightMatch = svgOpenTag.match(/height=["']([^"']+)["']/i);

  if (widthMatch && heightMatch) {
    const width = widthMatch[1].replace(/px$/i, "");
    const height = heightMatch[1].replace(/px$/i, "");
    return `0 0 ${width} ${height}`;
  }

  throw new Error(`SVG is missing a viewBox and width/height fallback: ${svgOpenTag}`);
}

export function extractIconAsset(filePath, fileName) {
  const rawSvg = readFileSync(filePath, "utf-8").trim();
  const svgMatch = rawSvg.match(/<svg\b([^>]*)>([\s\S]*?)<\/svg>/i);

  if (!svgMatch) {
    throw new Error(`Invalid SVG file: ${fileName}`);
  }

  const svgOpenTag = svgMatch[1];
  const innerBody = svgMatch[2]
    .replace(/<title\b[\s\S]*?<\/title>/gi, "")
    .replace(/<desc\b[\s\S]*?<\/desc>/gi, "")
    .replace(/<!--([\s\S]*?)-->/g, "")
    .trim();

  return {
    name: normalizeIconName(fileName),
    sourceFile: fileName,
    viewBox: extractViewBox(svgOpenTag),
    body: innerBody,
  };
}

export function assertUniqueIconNames(icons) {
  const seen = new Map();

  for (const icon of icons) {
    const previous = seen.get(icon.name);
    if (previous) {
      throw new Error(
        `Icon name collision for \"${icon.name}\": ${previous} and ${icon.sourceFile}`,
      );
    }
    seen.set(icon.name, icon.sourceFile);
  }
}

function normalizeSvgReferenceId(value) {
  return value.replace(/[^a-zA-Z0-9_.:-]+/g, "-");
}

function replaceSvgReference(value, idMap) {
  let nextValue = value;

  for (const [originalId, namespacedId] of idMap.entries()) {
    const escapedId = originalId.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");

    nextValue = nextValue
      .replace(
        new RegExp(`(["'])#${escapedId}(["'])`, "g"),
        `$1#${namespacedId}$2`,
      )
      .replace(
        new RegExp(`url\\((['"]?)#${escapedId}\\1\\)`, "g"),
        (_, quote = "") => `url(${quote}#${namespacedId}${quote})`,
      );
  }

  return nextValue;
}

export function namespaceSvgBodyIds(body, iconName) {
  const ids = [...body.matchAll(/\bid=(['"])([^'"]+)\1/g)].map((match) => match[2]);

  if (ids.length === 0) {
    return body;
  }

  const idMap = new Map(
    [...new Set(ids)].map((id) => [id, `${iconName}__${normalizeSvgReferenceId(id)}`]),
  );

  let nextBody = body.replace(/\bid=(['"])([^'"]+)\1/g, (match, quote, id) => {
    const namespacedId = idMap.get(id);
    return namespacedId ? `id=${quote}${namespacedId}${quote}` : match;
  });

  nextBody = replaceSvgReference(nextBody, idMap);

  return nextBody;
}

function loadAssets() {
  if (!existsSync(ASSETS_DIR)) {
    throw new Error(`Assets directory not found: ${ASSETS_DIR}`);
  }

  const svgFiles = readdirSync(ASSETS_DIR)
    .filter((file) => extname(file).toLowerCase() === ".svg")
    .sort((a, b) => a.localeCompare(b));

  if (svgFiles.length === 0) {
    throw new Error(`No SVG assets found in ${ASSETS_DIR}`);
  }

  const icons = svgFiles.map((fileName) =>
    extractIconAsset(join(ASSETS_DIR, fileName), fileName),
  );

  assertUniqueIconNames(icons);

  return icons;
}

export function generateSprite(icons) {
  const symbols = icons
    .map((icon) => {
      const namespacedBody = namespaceSvgBodyIds(icon.body, icon.name);
      return `  <symbol id="${icon.name}" viewBox="${icon.viewBox}">\n${indent(namespacedBody, 4)}\n  </symbol>`;
    })
    .join("\n");

  return [
    "<!-- Auto-generated by scripts/generate-cloudflare-icons.js. Do not edit directly. -->",
    '<svg xmlns="http://www.w3.org/2000/svg" aria-hidden="true" style="position:absolute;width:0;height:0;overflow:hidden">',
    symbols,
    "</svg>",
    "",
  ].join("\n");
}

export function generateNamesModule(icons) {
  const names = icons.map((icon) => `  \"${icon.name}\",`).join("\n");
  const viewBoxes = icons
    .map((icon) => `  \"${icon.name}\": \"${icon.viewBox}\",`)
    .join("\n");

  return [
    "/**",
    " * Auto-generated by scripts/generate-cloudflare-icons.js.",
    " * Source of truth: src/icons/assets/*.svg",
    " */",
    "",
    "export const cloudflareIconNames = [",
    names,
    "] as const;",
    "",
    "export type CloudflareIconName = (typeof cloudflareIconNames)[number];",
    "",
    "export const cloudflareIconViewBoxes = {",
    viewBoxes,
    "} as const satisfies Record<CloudflareIconName, string>;",
    "",
  ].join("\n");
}

export function indent(value, spaces) {
  const prefix = " ".repeat(spaces);
  return value
    .split("\n")
    .map((line) => (line.trim().length > 0 ? `${prefix}${line.trim()}` : line.trim()))
    .join("\n");
}

export function main() {
  const icons = loadAssets();

  mkdirSync(GENERATED_DIR, { recursive: true });

  writeFileSync(SPRITE_PATH, generateSprite(icons));
  writeFileSync(NAMES_PATH, generateNamesModule(icons));

  console.log("✓ Generated Cloudflare icon artifacts");
  console.log(`  → Assets: ${icons.length}`);
  console.log(`  → Sprite: ${SPRITE_PATH}`);
  console.log(`  → Names: ${NAMES_PATH}`);
}

if (process.argv[1] && resolve(process.argv[1]) === __filename) {
  main();
}
